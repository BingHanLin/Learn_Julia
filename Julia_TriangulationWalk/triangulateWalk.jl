# ╔═╡ adf5b2fd-ebb0-48d7-b46f-173d6f5955b2
function walking_figure(tri_mesh::Triangulation, walking_tri_id::Vector{Int64})::Figure

	all_tri_points = [Point(point[1], point[2]) for point in eachrow(tri_mesh.points)]

	all_tri_facets = [TriangleFace(vertices[1], vertices[2], vertices[3]) for vertices in eachrow(tri_mesh.simplices)]
	
	walking_tri_facets = [TriangleFace(tri_mesh.simplices[id, :][1], tri_mesh.simplices[id, :][2], tri_mesh.simplices[id, :][3])  for id in walking_tri_id]

	
	background_mesh = GeometryBasics.Mesh(all_tri_points, all_tri_facets)
	walking_mesh = GeometryBasics.Mesh(all_tri_points, walking_tri_facets)
	
	
	figure, _, _ = Makie.wireframe(background_mesh, figure = (resolution = (600, 400),))
	
	Makie.wireframe!(walking_mesh)

	step_number = Array{String}(undef, length(walking_tri_id))
	step_position = Array{Tuple{Float64, Float64}}(undef, length(walking_tri_id))
		

	for i in 1:length(walking_tri_id)
		tri_id = walking_tri_id[i]
		centroid = (tri_mesh.points[tri_mesh.simplices[tri_id, 1], :] +
					tri_mesh.points[tri_mesh.simplices[tri_id, 2], :] +
					tri_mesh.points[tri_mesh.simplices[tri_id, 3], :] ) /3.0

		step_number[i] = string(i)
		step_position[i] = (centroid[1], centroid[2])
	end
	
	text!(step_number, position = step_position, align = (:center, :center))

	return figure
end

