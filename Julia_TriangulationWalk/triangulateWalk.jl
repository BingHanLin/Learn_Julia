function orthogonal_walk(tri_mesh::Triangulation, query_point::Vector{Float64}, tri_id::Int64)::Int64
	
	q = tri_mesh.simplices[tri_id, 1]
	r = tri_mesh.simplices[tri_id, 2]
	l = tri_mesh.simplices[tri_id, 3]

	intermediate_point = [query_point[1], tri_mesh.points[q, 2]]
	
	# initial step
	if(tri_mesh.points[r, 2] < tri_mesh.points[q, 2])
		while(tri_mesh.points[l, 2] < tri_mesh.points[q, 2])
			r = l
			tri_id = neighor_triangle(tri_mesh, tri_id, q, l)
			println(tri_id)
			
			l = vertex3_on_triangle(tri_mesh, tri_id, q, r)	
		end
	else
		while true
			l = r
			tri_id = neighor_triangle(tri_mesh, tri_id, q, r)
			println(tri_id)
			
			r = vertex3_on_triangle(tri_mesh, tri_id, q, l)

		    if (tri_mesh.points[r, 2] < tri_mesh.points[q, 2])
				break
			end
		end
	end
	
	println("horizontal walk")


	# walk step in horizontal
	while((tri_mesh.points[r, 1] < intermediate_point[1] && tri_mesh.points[l, 1] < intermediate_point[1] ) ||
			orientation(intermediate_point, tri_mesh.points[r, :], tri_mesh.points[l, :]) < 0.0)
		tri_id = neighor_triangle(tri_mesh, tri_id, r, l)
		println(tri_id)
		s = vertex3_on_triangle(tri_mesh, tri_id, r, l)
		
		if(tri_mesh.points[s, 2] > tri_mesh.points[q, 2])
			l = s
		else
			r = s
		end
	end

	println("vertical walk")
	
	# walk step in vertical
	while((tri_mesh.points[r, 2] < query_point[2] && tri_mesh.points[l, 2] < query_point[2] ) ||
			orientation(query_point, tri_mesh.points[r, :], tri_mesh.points[l, :]) < 0.0)
		tri_id = neighor_triangle(tri_mesh, tri_id, r, l)
		println(tri_id)
		
		s = vertex3_on_triangle(tri_mesh, tri_id, r, l)

		if(tri_mesh.points[s, 1] < query_point[1])
			l = s
		else
			r = s
		end
	end
	
	return tri_id
end

