# ╔═╡ 464c6219-a8b3-4cec-aca4-5631bd6d0987
function visibility_walk(tri_mesh::Triangulation, query_point::Vector{Float64}, tri_id::Int64)::Vector{Int64}

	path_tri_id =[]
	push!(path_tri_id, tri_id)
	
	isEnd = false
	entry_edge = (-1, -1)
	
	while(!isEnd)

		edge_list = random_edge_list(tri_mesh, tri_id)

		tri_vertices = tri_mesh.simplices[tri_id, :]
		tri_centroid = (tri_mesh.points[tri_vertices[1], :] +tri_mesh.points[tri_vertices[2], :] + tri_mesh.points[tri_vertices[3], :])/3.0

		counter = 0
		for test_edge in edge_list
			if (!is_same_edge(entry_edge, test_edge) && !is_same_side(tri_centroid, query_point, tri_mesh.points[test_edge[1], :], tri_mesh.points[test_edge[2], :]))

				neighbor_tri_id = neighor_triangle(tri_mesh, tri_id, test_edge[1], test_edge[2])	
				if(neighbor_tri_id<1)
					counter += 1
				else
					tri_id = neighbor_tri_id
					entry_edge = test_edge

					push!(path_tri_id, tri_id)
				end
				break
			else
				counter += 1
			end
		end

		if (counter == 3)
			isEnd =true
		end
	end
	
	return path_tri_id
end

