# ╔═╡ 646386ba-cbca-470e-9fd4-3bdf99bb6e54
function straight_walk(tri_mesh::Triangulation, query_point::Vector{Float64}, tri_id::Int64)::Vector{Int64}

	path_tri_id =[]
	
	q = tri_mesh.simplices[tri_id, 1]
	r = tri_mesh.simplices[tri_id, 2]
	l = tri_mesh.simplices[tri_id, 3]

	# initial step
	if(orientation(tri_mesh.points[r, :], tri_mesh.points[q, :], query_point) < 0.0)
		while(orientation(tri_mesh.points[l, :], tri_mesh.points[q, :], query_point) < 0.0)

			r = l
			tri_id = neighor_triangle(tri_mesh, tri_id, q, l)
			l = vertex3_on_triangle(tri_mesh, tri_id, q, r)	
		end
	else
		while true
			l = r
			tri_id = neighor_triangle(tri_mesh, tri_id, q, r)
			r = vertex3_on_triangle(tri_mesh, tri_id, q, l)
			
		    if (orientation(tri_mesh.points[r, :], tri_mesh.points[r, :], tri_mesh.points[l, :]) > 0.0)
				break
			end
		end
	end

	push!(path_tri_id, tri_id)

	println("sss")
	
	# walk step
	while(orientation(query_point, tri_mesh.points[r, :], tri_mesh.points[l, :]) < 0.0)
		tri_id = neighor_triangle(tri_mesh, tri_id, r, l)
		s = vertex3_on_triangle(tri_mesh, tri_id, r, l)

		push!(path_tri_id, tri_id)

		println("ttt")
		
		if(orientation(tri_mesh.points[s, :], tri_mesh.points[q, :], query_point) < 0.0)
			r = s
		else
			l = s
		end
	end

	return path_tri_id
end

