# ╔═╡ af9d3df4-34ef-418b-bd14-2893a6b22d22
function orientation(r::Vector{Float64}, q::Vector{Float64}, p::Vector{Float64})::Float64
	
	A = [q[1]-r[1] p[1]-r[1]; q[2]-r[2] p[2]-r[2]]
	return det(A)
end

# ╔═╡ 00c43033-bdb3-427f-a162-cf69f8986f44
md"""
### check same side function
check if points on the same side of edge
"""

# ╔═╡ 1f9ae5ff-67d2-4ad3-8688-51acd9c3b115
function is_same_side(point1::Vector{Float64}, point2::Vector{Float64}, edge_point1::Vector{Float64}, edge_point2::Vector{Float64})::Bool
	
	return sign(orientation(point1, edge_point1, edge_point2)) == sign(orientation(point2, edge_point1, edge_point2))
end

# ╔═╡ c3ce4b54-f193-459e-a05a-29a9102821d3
function neighor_triangle(tri_mesh::Triangulation, tri_id::Int64, edge_index::Int64)::Int64
	
	return tri_mesh.neighbors[tri_id, edge_index]
end

# ╔═╡ 9c41f970-0e19-469a-a7a2-e71248b9f7bb
md"""
### neighbor triangle function
get neighbor triangle id across an edge
"""

# ╔═╡ 324465fb-e57a-44d9-baa5-506bf3dcd080
function neighor_triangle(tri_mesh::Triangulation, tri_id::Int64, vertex1_id::Int64, vertex2_id::Int64)::Int64
	
	for i in 1:3
		nighbor_id = tri_mesh.neighbors[tri_id, i]
		
		if(nighbor_id<1)
			continue
		end
		
		if(vertex1_id in Set(tri_mesh.simplices[nighbor_id, :]) &&
	   		vertex2_id in Set(tri_mesh.simplices[nighbor_id, :]))
			return nighbor_id
		end	
	end
	
	return -1
end

# ╔═╡ 8d591f61-61a5-42c9-97d8-35cb4c1724ba
md"""
