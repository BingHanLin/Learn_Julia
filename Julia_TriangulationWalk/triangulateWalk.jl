# ╔═╡ e0881d50-e767-4185-aee0-de3a47d752eb
function is_neighbor_of_tri(tri_mesh::Triangulation, neighbor_tri_id::Int64, tri_id::Int64, vertex1_id::Int64, vertex2_id::Int64)::Bool
	return (neighbor_tri_id==neighor_triangle(tri_mesh, tri_id, vertex1_id, vertex2_id))
end

# ╔═╡ e107ab8f-f2da-4e10-8688-de38778f68ee
md"""
### third vertex of triangle
