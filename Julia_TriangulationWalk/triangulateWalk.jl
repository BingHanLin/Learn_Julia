# ╔═╡ 9dad08d7-f894-42ea-815c-6ffab138b1f1
function vertex3_on_triangle(tri_mesh::Triangulation, tri_id::Int64, vertex1_id::Int64, vertex2_id::Int64)::Int64
	
	for vertex_id in tri_mesh.simplices[tri_id,:]
		if (vertex_id != vertex1_id) && (vertex_id!= vertex2_id)
			return vertex_id
		end
	end
	return -1
end

