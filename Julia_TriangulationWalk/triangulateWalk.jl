# ╔═╡ 5f4b4912-fa36-45ec-9bdd-f493c5d5a85a
function random_edge_list(tri_mesh::Triangulation, tri_id::Int64)

	tri_vertices = tri_mesh.simplices[tri_id, :]

	edge_1 = tri_vertices[1], tri_vertices[2]
	edge_2 = tri_vertices[2], tri_vertices[3]
	edge_3 = tri_vertices[3], tri_vertices[1]

	edges = edge_1, edge_2, edge_3

	indices = [1, 2, 3]
	shuffle!(indices)

	return (edges[indices[1]], edges[indices[2]], edges[indices[3]])
end

