# ╔═╡ 3b987317-0d9a-46dd-99f6-0970fa851b3d
function is_same_edge(edge_1::Tuple{Int64, Int64}, edge_2::Tuple{Int64, Int64})::Bool
	return Set(edge_1) == Set(edge_2)
end

